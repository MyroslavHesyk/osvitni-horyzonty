export function getActiveDay() {
  const params = new URLSearchParams(window.location.search);
  const dayFromUrl = params.get("day");
  if (dayFromUrl) {
    const found = days.find((day) => day.id === dayFromUrl);
    if (found) return found;
  }

  const today = new Date();
  const todayKey = today.toISOString().slice(0, 10);
  const exact = days.find((day) => day.id === todayKey);
  if (exact) return exact;

  const first = days[0];
  const last = days[days.length - 1];
  if (todayKey < first.id) return first;
  if (todayKey > last.id) return last;
  return first;
}
